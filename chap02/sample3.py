# 代表的な演算子の使用例
print(12 + 5)  # 和 => 17
print(12 - 5)  # 差 => 7
print(12 * 5)  # 積 => 60
print(12 // 5) # 商 => 2
print(12 % 5)  # 余り => 2
print(12 / 5)  # 実数としての割り算 => 2.4
print(2**3)    # べき乗 => 8

# 数学関数のために必要
import math

print(math.exp(0.5))
print(math.log(2.0))
